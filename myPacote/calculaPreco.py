def aumentoP(Myvalor, Mypercent):
    resultado = float(Myvalor) + float(Myvalor) * (Mypercent/100)
    return print(f'\t R$ {resultado:.2f}'.replace('.', ','))


def reduzP(Myvalor, Mypercent):
    resultado = float(Myvalor) + float(Myvalor) * (Mypercent/100)
    return print(f'\t R$ {resultado:.2f}'.replace('.', ','))
